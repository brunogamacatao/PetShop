    private void botaoNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoNovoActionPerformed
        campoNome.setText(""); // limpando o campo nome
        campoTipo.setText(""); // limpando o campo tipo
        campoNome.requestFocus();
        selecionado = null;
        botaoExcluir.setEnabled(false);
    }//GEN-LAST:event_botaoNovoActionPerformed

    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        String nome = campoNome.getText();
        String tipo = campoTipo.getText();
        
        if (selecionado == null) { // adicionar
            Animal novo = new Animal(nome, tipo);
            try {
                // Adiciono o animal
                sistema.adicionarAnimal(novo);
                
                // zerar o selecionado
                selecionado = null;
                botaoExcluir.setEnabled(false);

                animaisTableModel.fireTableDataChanged(); // atualiza a tabela

                // Limpar o formul√°rio
                campoNome.setText(""); // limpando o campo nome
                campoTipo.setText(""); // limpando o campo tipo
                campoNome.requestFocus();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else { // alterar
            selecionado.setNome(nome);
            selecionado.setTipo(tipo);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void tabelaAnimaisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaAnimaisMouseClicked
        int indice = tabelaAnimais.getSelectedRow();
        selecionado = sistema.getAnimalPorIndice(indice);
        campoNome.setText(selecionado.getNome());
        campoTipo.setText(selecionado.getTipo());
        botaoExcluir.setEnabled(true);
    }//GEN-LAST:event_tabelaAnimaisMouseClicked

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        sistema.removerAnimal(selecionado);
        animaisTableModel.fireTableDataChanged();
    }//GEN-LAST:event_botaoExcluirActionPerformed

